  <script>
  export default {
    name: "CounterSalary",
    data() {
      return {
        counterId: localStorage.getItem("counterId"),
        month: new Date().toISOString().slice(0, 7),
        salaries: [],
        detailVisible: false,
        detailMonth: '',
        detail: {},
        activeSection: 'salary',
        activeSubsection: null,
      };
    },
    created() {
      this.fetchSalaryList();
    },
    methods: {
        async fetchSalaryList() {
  try {
    const employeeId = this.counterId;
    const employeeType = 'counter';
    const res = await fetch(`/api/salaries/list?employeeId=${employeeId}&employeeType=${employeeType}`);
    const json = await res.json();
    if (json.status === "success") {
      this.salaries = json.data;  
    } else {
      alert(json.message || "获取工资失败");
    }
  } catch (e) {
    alert("请求错误：" + e.message);
  }
},
      async generateSalary() {
        try {
          const res = await fetch(
            `/api/salaries/generate?employeeId=${this.counterId}&employeeType=counter&branchId=1&month=${this.month}`,
            { method: "POST" }
          );
          const json = await res.json();
          if (json.status === "success") {
            alert("生成成功");
            this.fetchSalaryList();
          } else {
            alert(json.message || "生成失败");
          }
        } catch (e) {
          alert("请求错误：" + e.message);
        }
      },
      fmt(val) {
        return parseFloat(val).toFixed(2);
      },
      logout() {
        localStorage.removeItem("counterId");
        this.$router.push("/login");
      },
      async showDetail(month) {
        this.detailVisible = true;
        this.detailMonth = month;
        try {
          const res = await fetch(
            `/api/salaries/detail?employeeId=${this.counterId}&employeeType=counter&month=${month}`
          );
          const json = await res.json();
          if (json.status === "success") {
            this.detail = json.data;
          } else {
            alert(json.message || "获取明细失败");
            this.detail = {};
            this.detailVisible = false;
          }
        } catch (e) {
          alert("请求错误：" + e.message);
          this.detail = {};
          this.detailVisible = false;
        }
      },
      selectSection(section) {
        this.activeSection = section;
        if (section === 'salary') {
          // 当前页面，无跳转
        } else if (section === 'profile') {
          this.$router.push('/counter-dashboard');
        }
      }
    }
  };
  </script>
  
